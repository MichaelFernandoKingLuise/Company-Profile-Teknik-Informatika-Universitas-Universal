                <p class="isi-paragraf">Program Studi Teknik Informatika Universitas Universal (UVERS) berfokus pada
                    bidang
                    rekayasa teknologi informasi berupa pengembangan program aplikasi yang ramah pengguna. Hal tersebut
                    memungkinkan para mahasiswa membuat program aplikasi dengan pendekatan algoritma yang lebih efisien
                    dengan desain tampilan aplikasi yang sangat menarik dan mudah digunakan, dengan kata lain unsur
                    estetika juga sangat diperhatikan dalam pemrograman aplikasi. Inilah yang menjadi ciri khas program
                    studi teknik informatika Uvers. Untuk menunjang ke-khas-an tersebut maka matakuliah Desain dan
                    Multimedia menjadi matakuliah utama selain algoritma dan pemrograman.
                </p>
                <p class="isi-paragraf">Dalam bidang pemrograman aplikasi, Program Studi Teknik Informatika UVERS
