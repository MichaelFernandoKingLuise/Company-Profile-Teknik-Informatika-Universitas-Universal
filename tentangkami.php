                    kebutuhan industri yang sangat besar terhadap bidang tersebut. Mahasiswa Program Studi Teknik
                    Informatika UVERS juga dibekali dengan kemampuan keamanan komputer dan jaringan agar dapat pula
                    membuat aplikasi-aplikasi berbasis jaringan (seperti cloud computing, client-server programming)
                    serta mampu melakukan pengujian terhadap keamanan aplikasi yang dibuatnya agar tidak mudah dibobol
                    oleh pihak lain. Selain itu, mengikuti perkembangan jaman, mahasiswa Program Studi Teknik
                    Informatika UVERS dibekali dengan pengetahuan terkait Artificial Intelligence. Dengan spesifikasi
                    ini, para lulusan akan memiliki nilai tawar yang lebih baik di dunia kerja.
                </p>
                <p class="isi-paragraf">Dengan berbagai keahlian tersebut, para lulusan dapat berkarir sebagai Desainer
